		<h3>1. What types of items do you accept for upcycling?</h3>
		<p>We accept small, handheld items with a maximum size of 3 feet in height and 3 feet in width. 
		This includes items like small furniture, decorative pieces, and similar items that fit within our size requirements.</p>
		<h3>2. Are there any materials or items you don’t accept?</h3>
		<p>Yes, we don't accept large or bulky items beyond 3 feet in height and width. 
		Additionally, items that are hazardous, extremely fragile, or contain complex electronics may not be suitable for upcycling. 
		If you’re unsure, please reach out to us to confirm.</p>
		<h3>3. How does the upcycling process work?</h3>
		<p>Once you provide us with the item, we work with local artisans to repurpose or transform it into something new based on your preferences. 
		We'll keep you updated throughout the process to ensure the final product aligns with your vision.</p>
		<h3>4. Can I request a specific design or style for my item?</h3>
		<p>Absolutely! We encourage customers to share any specific ideas, styles, or design preferences they have. 
		Our artisans will work to create a unique piece that matches your vision as closely as possible.</p>
		<h3>5. How long does the upcycling process take?</h3>
		<p>The timeframe depends on the complexity of the project and the artisan’s availability. Generally, smaller items take about 3-5 days to complete. 
		We will give you an estimated completion date once we assess your item.</p>
		<h3>6. How do I deliver my item to Revamp Hub?</h3>
		<p>We can arrange a pickup and delivery service for a small additional fee within our service area. 
		Please contact us to discuss delivery options.</p>
		<h3>7. What is the cost of upcycling an item?</h3>
		<p>Pricing varies based on the item and the complexity of the upcycling process. 
		Once we assess your item and understand your design preferences, we’ll provide a detailed quote.</p>
		<h3>8. What if I’m not satisfied with the final product?</h3>
		<p>Customer satisfaction is our priority. If you’re not satisfied with the final result, please let us know, 
		and we will work with you to make any reasonable adjustments to meet your expectations.</p>
		<h3>9. Can I see examples of past upcycling projects?</h3>
		<p>Yes! We showcase “before and after” transformations on our website. 
		Feel free to browse our artisan's gallery for inspiration.</p>
		<h3>10. How do I start a project with Revamp Hub?</h3>
		<p>Simply fill out our project inquiry form on the website, or contact us directly. 
		Share details about your item, and we’ll guide you through the next steps to get started.</p>
		
